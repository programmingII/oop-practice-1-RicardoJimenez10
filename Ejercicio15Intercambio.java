public class Ejercicio15Intercambio {
 
 public static void main(String[] args) {
     
   int a, b, temp;
   a = 15;
   b = 27;
   System.out.println("Antes del intercambio : a, b = "+a+", "+ + b);
   temp = a;
   a = b;
   b = temp;   
  System.out.println("Despues del intercambio : a, b = "+a+", "+ + b);
 }
 }
